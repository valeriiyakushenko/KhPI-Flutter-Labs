class Resume {
  final String id;
  final String name;
  final String email;
  final String phone;
  final String location;
  final String title;
  final String description;
  final String avatarUrl;


  Resume({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.location,
    required this.title,
    required this.description,
    required this.avatarUrl,
  });
}
